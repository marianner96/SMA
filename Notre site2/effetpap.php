    <div id="container">

        <div class="text">
          Henri Poincaré travailla sur des phénomènes chaotiques, et il constata que le déterminisme absolu ne reflète qu’un état particulier du 
          développement de la Science. Il était simple d’étudier d’abord les phénomènes les plus prévisibles (comme la chute des corps, le lever du Soleil, 
          le retour périodique de la Pleine Lune, des saisons, des marées, etc...) et une généralisation plutôt tentante, mais trop large, conduisait à considérer 
          que tous les phénomènes naturels devaient être déterministes. 
        </div>

        <div class="text">
          Il présenta donc des objections à l’idée du déterminisme absolu : Une cause très petite, qui nous échappe, détermine un effet considérable que 
          nous ne pouvons pas ne pas voir, et alors nous disons que cet effet est dû au hasard...Mais, alors même que les lois naturelles n’auraient plus de 
          secret pour nous, nous ne pourrons connaître la situation initiale qu’approximativement. Si cela nous permet de prévoir la situation ultérieure avec 
          la même approximation, c’est tout ce qu’il nous faut, nous dirons que le phénomène a été prévu, qu’il est régit par des lois ; mais il n’en est pas 
          toujours ainsi. Il peut arriver que de petites différences dans les conditions initiales en engendrent de très grandes dans les phénomènes finaux...
        </div>

        <div class="text">
          Le déterminisme mathématique suit la définition : « Deux expériences avec exactement les mêmes conditions initiales et les mêmes conditions aux 
          limites doivent donner exactement les mêmes résultats » et le modèle mathématique d'un phénomène sera considéré comme déterministe si les conditions 
          d'existence et d'unicité des solutions sont satisfaites, ce qui est généralement le cas pour les modèles utilisant des systèmes d'équations différentielles.
        </div>

        <div class="text">
          Est ce que cette instabilité, cet indéterminisme physique, cette sensibilité aux conditions initiales sont courants ? Henri Poincaré avait donné 
          quelques exemples, mais il est aussi l'initiateur de ce que nous appelons aujourd'hui la théorie du chaos dont la sensibilité aux conditions initiales 
          est l'élément essentiel, et il a reconnu que le chaos apparaît extrêmement souvent : il apparaît dans presque tous les problèmes non intégrables.
        </div>

        <div class="text">
          L’effet papillon se fait-il sentir dans la météorologie ? Lorenz ne s’engage pas sur ce point. Mais il a découvert que dans les systèmes météorologiques, 
          une infime variation d'un élément peut s'amplifier progressivement, jusqu'à provoquer des changements énormes au bout d'un certain temps.
        </div>

        <div class="text">
          Ensuite, il a été confirmé que l'analyse scientifique de l'évolution imprévisible des systèmes météorologiques(ce qui empêche la vision de leur évolution 
          à court terme, environ 8 jours) attribuait plutôt ce fait à un comportement inhérent à ces systèmes, et non à la méconnaissance des conditions initiales. 
          Par ailleurs, de tels systèmes, comme d'autres systèmes chaotiques, sont en fait caractérisés par l'émergence d'une stabilité à moyen et long terme (mois, 
          années) et non d'un chaos de plus en plus grand.
        </div>

        <div class="text">
          En clair, même si le météorologue ne peut pas prévoir le temps qu’il fera à Lyon dans un mois, il devrait être possible de prévoir des moyennes, des 
          fréquences d’événements météorologiques, avec une bonne précision, en un endroit donné, sur une longue période de temps. Bien sûr, ce type de prédiction 
          est plus modeste, mais il est souvent tout aussi utile.
        </div>

        <div class="text">
          Aujourd’hui, cette notion ne concerne pas seulement la cas particulier de la météorologie, elle a été étudiée dans différents domaines. Si on l'applique 
          aux sociétés humaines, cela voudrait dire que des changements de comportement qui semblent insignifiants au départ peuvent déclencher des bouleversements 
          à grande échelle. Donc si le futur est déterminé par le passé, il ne l’est peut-être pas d’une manière aussi naïve qu’on y pensait auparavant. Même si le 
          papillon du Brésil s’avérait impuissant, il existe de nombreux autres domaines où cette idée peut s’appliquer : les planètes, la finance, l'histoire, etc...
        </div>
